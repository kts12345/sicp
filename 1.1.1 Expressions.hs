-- sicp.1.1.1 Expressions

main = do
    print 486   -- 486
    print $ (+) 137 349  -- (+ 137 349)
    print $ (-) 1000 334 -- (- 1000 334)
    print $ (*) 5 99     -- (* 5 99)
    print $ (/) 10 5     -- (/ 10 5)
    print $ (+) 2.7 10   -- (+ 2.7 10)
    print $ foldr (+) 0 [21, 35, 12, 7] -- (+ 21 35 12 7)
    print $ foldr (*) 1 [25, 4, 12] -- (* 25 4 12)
    print $ (+) ((*) 3 5) ((-) 10 6) --(+ (* 3 5) (- 10 6))
    print $ (+) ((*) 3 ((+) ((*) 2 4) ((+) 3 5))) ((+) ((-) 10 7) 6) -- (+ (* 3 (+ (* 2 4) (+ 3 5))) (+ (- 10 7) 6))