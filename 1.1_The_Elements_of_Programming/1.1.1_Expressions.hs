-- sicp.1.1.1 Expressions
import Prelude hiding((++), (**))

-- define multi input (+)
(++) = foldr (+) 0

-- define multi input (*)
(**) = foldr (*) 1

main = do

    --      486
    print   486

    --      (+ 137 349)
    print $ (+) 137 349

    --      (- 1000 334)
    print $ (-) 1000 334

    --      (* 5 99)
    print $ (*) 5 99

    --      (/ 10 5)
    print $ (/) 10 5

    --      (+ 2.7 10)
    print $ (+) 2.7 10

    --      (+ 21 35 12 7)
    print $ (++) [21, 35, 12, 7]

    --      (* 25 4 12)
    print $ (**) [25, 4, 12]

    --      (+ (* 3 5) (- 10 6))
    print $ (+) ((*) 3 5) ((-) 10 6) 

    --      (+ (* 3 (+ (* 2 4) (+ 3 5))) (+ (- 10 7) 6))
    print $ (+) ((*) 3 ((+) ((*) 2 4) ((+) 3 5))) ((+) ((-) 10 7) 6) 